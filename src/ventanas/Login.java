    // BOTON PARA AUTENTIVCAR EL USUARIO
    private void btnaccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaccederActionPerformed
        // TODO add your handling code here:
        usuario = txtusuario.getText().trim();
        pass = pfpassword.getText().trim();
        
        // VALIDA QUE EL USUARIO Y PASSWORD EXISTAN
        if(!usuario.equals("") && !pass.equals("")){
            try{
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement("select id_empleado from usuarios where usuario = '" + usuario + "' and password = '" + pass + "'");
                
                ResultSet rs = pst.executeQuery();
                if(rs.next()){
                    dispose();
                    new Menu().setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Datos de acceso incorrecto");
                    txtusuario.setText("");
                    pfpassword.setText("");
                }
            }
            catch(Exception e){
                System.err.println("Error en el boton Acceder. " + e);
                JOptionPane.showMessageDialog(null, "Error al iniciar sesi√≥n, contacte al Administrador. ");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Debes de llenar Todos los campos.");
        }
        
        
    }//GEN-LAST:event_btnaccederActionPerformed

